package mypackage;

public class Operators {

	public static void main(String[] args) {
//		Arithmetic Operators
//		Addition
		int result = 1 + 2; // 1 + 2 = 3
		System.out.println(result);
//		Subtraction
		int changedResult = result - 1; // 3 - 1
		System.out.println(changedResult);
//		Multiplication
		result = result * 10; // 3 * 10 = 30
		System.out.println(result);
//		Division
		result = result / 10; // 30/10 = 3
		System.out.println(result);
//		Modulus
		result = result % 1; // 3 % 1 = 0
		System.out.println(result);
//		Abbreivating 
		result++;
		System.out.println(result);
		result--;
		System.out.println(result);
		result+=2;
		System.out.println(result);
		result*=10;
		System.out.println(result);
		result/=2;
		System.out.println(result);
		result-=7;
		System.out.println(result);
	
	}

}
